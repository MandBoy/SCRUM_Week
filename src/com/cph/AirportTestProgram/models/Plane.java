package com.cph.AirportTestProgram.models;

public class Plane{
    private int planeNumber;
    private int planeSize;
    private int stallNumber;

    public Plane(){
        planeNumber = planeNumber;
        planeSize = planeSize;
        stallNumber = stallNumber;
    }
    public void setPlaneNumber(int planeNumber) {
        this.planeNumber = planeNumber;
    }

    public void setPlaneSize(int planeSize) {
        this.planeSize = planeSize;
    }

    public void setStallNumber(int stallNumber) {
        this.stallNumber = stallNumber;
    }
    public int getPlaneNumber() {
        return planeNumber;
    }

    public int getPlaneSize() {
        return planeSize;
    }

    public int getStallNumber() {
        return stallNumber;
    }
}
